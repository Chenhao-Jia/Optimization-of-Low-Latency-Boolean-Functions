module sbox_KECCAK (
    input  [4:0] in,  // 5-bit input
    output reg [4:0] out  // 5-bit output
);
    always @(*) begin
        case (in)
            5'b00000: out = 5'b00000;  // '0'
            5'b00001: out = 5'b01001;  // '9'
            5'b00010: out = 5'b10010;  // '12'
            5'b00011: out = 5'b01011;  // 'b'
            5'b00100: out = 5'b00101;  // '5'
            5'b00101: out = 5'b01100;  // 'c'
            5'b00110: out = 5'b10110;  // '16'
            5'b00111: out = 5'b01111;  // 'f'
            5'b01000: out = 5'b01010;  // 'a'
            5'b01001: out = 5'b00011;  // '3'
            5'b01010: out = 5'b11000;  // '18'
            5'b01011: out = 5'b00001;  // '1'
            5'b01100: out = 5'b01101;  // 'd'
            5'b01101: out = 5'b00100;  // '4'
            5'b01110: out = 5'b11110;  // '1e'
            5'b01111: out = 5'b00111;  // '7'
	    5'b10000: out = 5'b10100;  // '14'
            5'b10001: out = 5'b10101;  // '15'
            5'b10010: out = 5'b00110;  // '6'
            5'b10011: out = 5'b10111;  // '17'
            5'b10100: out = 5'b10001;  // '11'
            5'b10101: out = 5'b10000;  // '10'
            5'b10110: out = 5'b00010;  // '2'
            5'b10111: out = 5'b10011;  // '13'
            5'b11000: out = 5'b11010;  // '1a'
            5'b11001: out = 5'b11011;  // '1b'
            5'b11010: out = 5'b01000;  // '8'
            5'b11011: out = 5'b11001;  // '19'
            5'b11100: out = 5'b11101;  // '1d'
            5'b11101: out = 5'b11100;  // '1c'
            5'b11110: out = 5'b01110;  // 'e'
            5'b11111: out = 5'b11111;  // '1f'
        endcase
    end
endmodule
