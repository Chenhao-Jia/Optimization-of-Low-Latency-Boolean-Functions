module sbox_KECCAK_inv (
    input  [4:0] X,   // 5-bit input
    output reg [4:0] Y // 5-bit output
);

always @(*) begin
    case (X)
        5'b00000: Y = 5'b00000; // 0x00
        5'b00001: Y = 5'b01011; // 0x0B
        5'b00010: Y = 5'b10110; // 0x16
        5'b00011: Y = 5'b01001; // 0x09
        5'b00100: Y = 5'b01101; // 0x0D
        5'b00101: Y = 5'b00100; // 0x04
        5'b00110: Y = 5'b10010; // 0x12
        5'b00111: Y = 5'b01111; // 0x0F
        5'b01000: Y = 5'b11010; // 0x1A
        5'b01001: Y = 5'b00001; // 0x01
        5'b01010: Y = 5'b01000; // 0x08
        5'b01011: Y = 5'b00011; // 0x03
        5'b01100: Y = 5'b00101; // 0x05
        5'b01101: Y = 5'b01100; // 0x0C
        5'b01110: Y = 5'b11110; // 0x1E
        5'b01111: Y = 5'b00111; // 0x07

        5'b10000: Y = 5'b10101; // 0x15
        5'b10001: Y = 5'b10100; // 0x14
        5'b10010: Y = 5'b00010; // 0x02
        5'b10011: Y = 5'b10111; // 0x17
        5'b10100: Y = 5'b10000; // 0x10
        5'b10101: Y = 5'b10001; // 0x11
        5'b10110: Y = 5'b00110; // 0x06
        5'b10111: Y = 5'b10011; // 0x13
        5'b11000: Y = 5'b01010; // 0x0A
        5'b11001: Y = 5'b11011; // 0x1B
        5'b11010: Y = 5'b11000; // 0x18
        5'b11011: Y = 5'b11001; // 0x19
        5'b11100: Y = 5'b11101; // 0x1D
        5'b11101: Y = 5'b11100; // 0x1C
        5'b11110: Y = 5'b01110; // 0x0E
        5'b11111: Y = 5'b11111; // 0x1F

        default: Y = 5'b00000;
    endcase
end

endmodule
